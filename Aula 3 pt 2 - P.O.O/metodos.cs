public class Metodos
{
    public double Somar(double n1, double n2)
    {
        return n1 + n2;
    }
    public double Subtrair(double n1, double n2)
    {
        return n1 - n2;
    }
    public double Multiplicar(double n1, double n2)
    {
        return n1 * n2;
    }
    public double Divisão(double n1, double n2)
    {
        return n1 / n2;
    }
}